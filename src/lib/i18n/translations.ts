export type Lang = keyof typeof translations
export type Dict = (typeof translations)['zh']

// 数据文件用的多语字段类型（zh / en / ru）
export type LangText = { zh: string; en: string; ru: string }
export type LangList = { zh: string[]; en: string[]; ru: string[] }

// 数据文件用：从 { zh, en, ru } 结构按当前语言取值
export function pick<T>(value: Record<Lang, T>, lang: Lang): T {
  return value[lang]
}
